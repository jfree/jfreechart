        clone.sectionPaintMap = new HashMap<>(this.sectionPaintMap);
        clone.sectionOutlinePaintMap = new HashMap<>(this.sectionOutlinePaintMap);
        clone.sectionOutlineStrokeMap = new HashMap<>(this.sectionOutlineStrokeMap);
